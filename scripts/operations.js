// Arithmetic operations
function sum(num1, num2) {
    const result = num1 + num2;
    return result;
}

function subtract(num1, num2) {
    const result = num1 - num2;
    return result;
}

function multiply(num1, num2) {
    const result = num1 * num2;
    return result;
}

function divide(num1, num2) {
    const result = num1 / num2;
    return result;
}

function divideOneByX(num) {
    const result = 1 / num;
    return result;
}

